# En esta sección conoceremos uno de los tipos de datos más útiles de python. Las listas
# Similar a los strings, las listas son secuencias de valores. En los strings los valores serían las letras, mientas que en una lista los valores pueden ser de cualquier tipo. Los valores en una lista se les llama elementos o items.
# La forma más directa de crear listas es con los corchetes []. Ejemplo

numbers = [10,20,30,40]
animals = ["dog", "cat","frog"]

# El primer ejemplo es una lista de solo números y el segundo solo de strings. Los elementos pueden ser de cualquier tipo

mixed = ["hola", 42, True, [10,20]]

# Incluso podemos tener listas dentro de listas o anidadas.

# Podemos crear una lista vacía solo con los []

empty = []

print(numbers, animals, mixed, empty)
