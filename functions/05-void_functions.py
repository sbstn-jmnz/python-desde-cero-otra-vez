# Algunas funciones nos entregan un valor para seguir utilizándolo en nuestro programa. Como la función input(), int() o randint().
# Todas ellas nos devuelven un valor.
# Pero hay otras funciones que simplemente realizan una operación pero no devuelven ningún valor como la función print()
# A estas funciones se le conoce como funciones void y en rigor devuelven el valor None. Que representa un valor nulo.

return_value = print("Hola Inmundo")
print(return_value)

other_return_value = int(3.5)
print(other_return_value)

# Las funciones que retornan valores no tienen un nombre en especial, solo las void que no tienen valor de retorno

